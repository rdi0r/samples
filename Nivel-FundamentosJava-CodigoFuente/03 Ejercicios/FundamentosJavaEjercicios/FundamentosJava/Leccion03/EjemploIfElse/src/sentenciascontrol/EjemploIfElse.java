package sentenciascontrol;

public class EjemploIfElse {

    public static void main(String[] args) {
        //Ejemplo If/Else
        int x = 30;
        if (x < 20) {
            System.out.print("X menos que 20\n");
        } else {
            System.out.print("X mayor que 20\n");
        }
    }
}
